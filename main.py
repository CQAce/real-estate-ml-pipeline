# Начнем строго по первому пункту вакансии: 
# "Делать EDA: искать закономерности... Умение аккуратно работать с грязными, неполными и шумными данными".

import numpy as np
import pandas as pd
import sqlite3

# ==========================================
# ЭТАП 1: Создаем "грязный" датасет объявлений
# ==========================================
dirty_data = {
    'ID':[1, 2, 3, 4, 5, 6, 6],
    'Район': ['Центр', 'Окраина', 'Центр', 'Окраина', 'Центр', 'Окраина', 'Окраина'],
    'Площадь_квм': [50, 45, np.nan, 60, 5000, 40, 40],          # У ID 3 — пропуск (NaN), у ID 5 — выброс (5000 кв.м.!)
    'Цена_млн_руб': [15.0, 5.0, 14.5, 7.0, 12.0, 4.8, 4.8]
}

df = pd.DataFrame(dirty_data)
print("=== ИСХОДНЫЙ ГРЯЗНЫЙ ДАТАСЕТ ===")
print(df)
print("-" * 50)

# ==========================================
# ЭТАП 2: Борьба с дубликатами
# ==========================================
# Удаляем полные дубликаты строк
df = df.drop_duplicates()

# ==========================================
# ЭТАП 3: Очистка от безумных выбросов
# ==========================================
# Мы знаем, что квартир больше 500 кв.м. в нашем сегменте быть не может.
# Фильтруем таблицу, оставляя только адекватную площадь
df = df[df['Площадь_квм'] < 500]

# ==========================================
# ЭТАП 4: Заполнение пропусков (Imputation)
# ==========================================
# У нас остался пропуск в площади у ID 3. Чем его заполнить? 
# Заполним МЕДИАНОЙ по всей площади, чтобы не искажать распределение
median_area = df['Площадь_квм'].median()
df['Площадь_квм'] = df['Площадь_квм'].fillna(median_area)

print("\n=== ОЧИЩЕННЫЙ ДАТАСЕТ (ГОТОВ К ML) ===")
print(df)




# Переходим к следующему важнейшему требованию вакансии: 
# «Готовить признаки... географические. Интерес к геоаналитике: координаты, расстояния, районы».


# Функция расчета расстояния на сфере (Земле) между двумя точками
def haversine_distance(lat1, lon1, lat2, lon2):
    # Переводим градусы в радианы
    lat1, lon1, lat2, lon2 = map(np.radians, [lat1, lon1, lat2, lon2])
    
    # Формула гаверсинусов
    dlat = lat2 - lat1
    dlon = lon2 - lon1
    a = np.sin(dlat/2)**2 + np.cos(lat1) * np.cos(lat2) * np.sin(dlon/2)**2
    c = 2 * np.arcsin(np.sqrt(a))
    
    # Радиус Земли в километрах (6371 км)
    km = 6371 * c
    return km

# ==========================================
# ЭТАП 1: Датасет квартир с координатами (Широта и Долгота)
# ==========================================
apartments = {
    'Название': ['Квартира на Тверской', 'Квартира в Люблино', 'Квартира в Бутово'],
    'Широта': [55.7601, 55.6755, 55.5421],
    'Долгота': [37.6085, 37.7612, 37.5543]
}
df = pd.DataFrame(apartments)

# Координаты центра Москвы (Кремль / Нулевой километр)
MOSCOW_CENTER_LAT = 55.7558
MOSCOW_CENTER_LON = 37.6173

# ==========================================
# ЭТАП 2: Генерация географического признака
# ==========================================
# Применяем функцию ко всей таблице Pandas и создаем НОВЫЙ СТОЛБЕЦ
df['Расстояние_до_центра_км'] = haversine_distance(
    df['Широта'], df['Долгота'], 
    MOSCOW_CENTER_LAT, MOSCOW_CENTER_LON
)

print("=== ТАБЛИЦА С НОВЫМ ГЕО-ПРИЗНАКОМ ===")
print(df[['Название', 'Расстояние_до_центра_км']])



# Переходим к следующему фундаментальному требованию вакансии: 
# «SQL: выборки, join, group by, оконные функции будут плюсом».


# 1. Создаем временную базу данных прямо в оперативной памяти машины
conn = sqlite3.connect(':memory:')
cursor = conn.cursor()

# 2. Создаем Таблицу 1: Информация о квартирах
cursor.execute('''
CREATE TABLE apartments (
    id INTEGER,
    area_sqm REAL,
    price_mln REAL,
    district_id INTEGER
)
''')

# Заполняем её данными
apartments_data = [
    (1, 50.0, 15.0, 101), # Квартира в районе 101
    (2, 45.0, 5.0,  102), # Квартира в районе 102
    (3, 60.0, 7.0,  102),
    (4, 75.0, 22.0, 101)
]
cursor.executemany('INSERT INTO apartments VALUES (?,?,?,?)', apartments_data)

# 3. Создаем Таблицу 2: Справочник названий районов
cursor.execute('''
CREATE TABLE districts (
    district_id INTEGER,
    district_name TEXT
)
''')

districts_data = [
    (101, 'Центр'),
    (102, 'Окраина')
]
cursor.executemany('INSERT INTO districts VALUES (?,?)', districts_data)
conn.commit()

# ==========================================
# ЭТАП 4: Пишем боевой SQL-запрос для выгрузки и аналитики
# ==========================================
# Запрос делает следующее:
# - Соединяет (JOIN) таблицы квартир и районов по совпадению ID района
# - Группирует (GROUP BY) по названию района
# - Считает среднюю цену квадратного метра для каждого района
sql_query = """
SELECT 
    d.district_name AS Район,
    AVG(a.price_mln / a.area_sqm) * 1000000 AS Средняя_цена_за_квм
FROM apartments a
JOIN districts d ON a.district_id = d.district_id
GROUP BY d.district_name
"""

# Запускаем SQL-запрос и сразу превращаем результат в красивый DataFrame Pandas
result_df = pd.read_sql_query(sql_query, conn)

print("=== РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ SQL-ЗАПРОСА ===")
print(result_df)

# Закрываем соединение с базой
conn.close()
