import numpy as np
import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.metrics import mean_absolute_error
from catboost import CatBoostRegressor # Импортируем модель от Яндекса

# Функция расчета расстояния до центра (наш гео-признак из Проекта 2)
def haversine_distance(lat1, lon1, lat2, lon2):
    lat1, lon1, lat2, lon2 = map(np.radians, [lat1, lon1, lat2, lon2])
    dlat = lat2 - lat1
    dlon = lon2 - lon1
    a = np.sin(dlat/2)**2 + np.cos(lat1) * np.cos(lat2) * np.sin(dlon/2)**2
    c = 2 * np.arcsin(np.sqrt(a))
    return 6371 * c

# Координаты центра (Кремль)
MOSCOW_CENTER_LAT = 55.7558
MOSCOW_CENTER_LON = 37.6173

# ==========================================
# ЭТАП 1: Загрузка и подготовка данных (EDA + Очистка)
# ==========================================
# Создаем симуляцию базы данных агентства недвижимости
raw_data = {
    'Район': ['Центр', 'Окраина', 'Центр', 'Окраина', 'Центр', 'Окраина', 'Центр', 'Окраина'],
    'Площадь_квм': [50.0, 45.0, 35.0, 60.0, 5000.0, 40.0, 85.0, 55.0], # 5000.0 - выброс!
    'Широта': [55.7601, 55.6755, 55.7421, 55.5421, 55.7558, 55.6122, 55.7699, 55.5511],
    'Долгота': [37.6085, 37.7612, 37.5543, 37.5543, 37.6173, 37.7432, 37.5911, 37.4922],
    'Цена_млн_руб': [15.0, 5.0, 11.5, 7.0, 12.0, 4.8, 26.0, 6.2]
}
df = pd.DataFrame(raw_data)

# Шаг А: Избавляемся от выброса по площади (наш опыт из Проекта 1)
df = df[df['Площадь_квм'] < 500]

# Шаг Б: Генерируем географический признак (Feature Engineering)
df['Расстояние_до_центра_км'] = haversine_distance(
    df['Широта'], df['Долгота'], MOSCOW_CENTER_LAT, MOSCOW_CENTER_LON
)

# Шаг В: Выбираем финальные признаки для модели и целевую переменную
X = df[['Район', 'Площадь_квм', 'Расстояние_до_центра_км']]
y = df['Цена_млн_руб']

# Шаг Г: Делим данные на обучение и валидацию
X_train, X_val, y_train, y_val = train_test_split(X, y, test_size=0.25, random_state=42)

# ==========================================
# ЭТАП 2: Обучение модели CatBoost
# ==========================================
# Говорим модели, какие столбцы содержат текст (категории)
cat_features = ['Район']

# Создаем модель. Нам не нужно много деревьев, так как датасет крошечный.
model = CatBoostRegressor(iterations=30, learning_rate=0.2, depth=3, random_state=42, verbose=0)

# Обучаем модель, передавая информацию о текстовых признаках
model.fit(X_train, y_train, cat_features=cat_features)

# ==========================================
# ЭТАП 3: Валидация и оценка качества
# ==========================================
val_predictions = model.predict(X_val)
mae = mean_absolute_error(y_val, val_predictions)

print(f"=== РЕЗУЛЬТАТЫ ВАЛИДАЦИИ ===")
print(f"Средняя ошибка модели (MAE) на проверке: {mae:.2f} млн руб.\n")

# ==========================================
# ЭТАП 4: Прогноз для НОВОЙ квартиры (Инвестиционная оценка)
# ==========================================
# Инвестор принес объект: Квартира на Окраине, 52 кв.м., координаты Бутово
new_apartment = pd.DataFrame({
    'Район': ['Окраина'],
    'Площадь_квм': [52.0],
    'Широта': [55.5410],
    'Долгота': [37.5520]
})

# Автоматически считаем для нее расстояние до центра, чтобы модель поняла геопозицию
new_apartment['Расстояние_до_центра_км'] = haversine_distance(
    new_apartment['Широта'], new_apartment['Долгота'], MOSCOW_CENTER_LAT, MOSCOW_CENTER_LON
)

# Оставляем только те столбцы, на которых училась модель
X_new = new_apartment[['Район', 'Площадь_квм', 'Расстояние_до_центра_км']]

# Делаем прогноз «справедливой цены»
predicted_price = model.predict(X_new)[0]

print(f"=== ИНВЕСТИЦИОННАЯ ОЦЕНКА ОБЪЕКТА ===")
print(f"🗺️ Район: Окраина, Площадь: 52 кв.м., До центра: {new_apartment['Расстояние_до_центра_км'][0]:.1f} км")
print(f"💰 Справедливая рыночная цена объекта: {predicted_price:.2f} млн руб.")
